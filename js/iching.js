  function getTrigram(num) {
    var n = num % 8;
    var trigrams = ['坤','震','坎','兌','艮','離','巽','乾'];
    return { name: trigrams[n], index: n };
  }

  /** 姓名起卦：上卦=人格, 下卦=地格, 動爻=總格 */
  function nameToHexagram(cnResult) {
    if (!cnResult) return null;
    var ren = cnResult.grids.ren.number;
    var di = cnResult.grids.di.number;
    var zong = cnResult.grids.zong.number;

    var upper = getTrigram(ren);
    var lower = getTrigram(di);
    var movingYao = (zong % 6) || 6;

    // 上卦*8 + 下卦 = 序號
    var hexIdx = upper.index * 8 + lower.index + 1;
    var hex = HEXAGRAMS[hexIdx];
    if (!hex) hex = HEXAGRAMS[1];

    return {
      hexIndex: hexIdx,
      hexName: hex.n,
      hexUnicode: hex.u,
      element: hex.el,
      glory: hex.g,
      description: hex.d,
      upperTrigram: upper.name,
      lowerTrigram: lower.name,
      movingYao: movingYao,
      analysis: '本卦「' + hex.n + '」：' + hex.d + '\n上卦' + upper.name + '（人格' + ren + '），下卦' + lower.name + '（地格' + di + '），動爻第' + movingYao + '爻。'
    };
  }

  /** 64卦列表 */
  function getAllHexagrams() { return HEXAGRAMS.slice(1); }

